package com.duweri.interview;

public class UseSwitch {
	public static void main(String[] args) {
		int i = 3;
		switch (i) {
		default:
			System.out.println("default");
			break;
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		case 3:
			System.out.println("3");
			break;
		}
	}

}
